#include <stdio.h>
#include <stdlib.h>

int main(int argc, char *argv[]) {
    
    int m; // 輸入分鐘數
    
    // 讀取輸入
    scanf("%d", &m);
    
    double cost; // 用來存放計算出來的電話費
    
    // 依照題目條件進行判斷
    if (m <= 800) {
        // 情況 1: 800 分鐘以下 (每分鐘 0.9 元)
        cost = m * 0.9;
    } else if (m < 1500) {
        // 情況 2: 介於 800 ~ 1500 分鐘之間
        // 題目說：所有電話費以 9 折計算
        // 原價是 (m * 0.9)，再乘以 0.9
        cost = m * 0.9 * 0.9;
    } else {
        // 情況 3: 1500 分鐘以上 (含 1500)
        // 題目說：通話費將以 79 折計算
        // 原價是 (m * 0.9)，再乘以 0.79
        cost = m * 0.9 * 0.79;
    }
    
    // 輸出結果 (只取到小數第一位，建議手動四捨五入以防萬一)
    // 邏輯：乘以 10 -> 加 0.5 (做四捨五入) -> 取整數 -> 除以 10.0
    cost = (int)(cost * 10 + 0.5) / 10.0;
    
    // 印出結果
    printf("%.1f\n", cost);

	return 0;
}