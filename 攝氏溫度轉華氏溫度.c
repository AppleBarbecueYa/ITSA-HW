#include <stdio.h>
#include <stdlib.h>

int main(int argc, char *argv[]) {
    double c; // 攝氏溫度 (題目說不一定是整數，所以用 double)
    
    // 讀取輸入
    scanf("%lf", &c);
    
    // 1. 計算華氏溫度
    // 公式：F = C * (9/5) + 32
    double f = c * (9.0 / 5.0) + 32.0;
    
    // 2. 處理四捨五入
    // 因為溫度可能是負數 (例如 -40度)，正數和負數的進位方向不同
    if (f >= 0) {
        // 正數：加 0.5 取整數 (例如 1.45)
        f = (int)(f * 10 + 0.5) / 10.0;
    } else {
        // 負數：減 0.5 取整數 (例如 -1.45)
        f = (int)(f * 10 - 0.5) / 10.0;
    }
    
    // 3. 輸出結果
    // 題目要求：小數點第一位，整數需補零 (例如 14.0)
    printf("%.1f\n", f);

	return 0;
}