#include <stdio.h>
#include <stdlib.h>

int main(int argc, char *argv[]) {
    int num; 
    
    scanf("%d", &num); //4位數整數
    
    // 題目要求：反向輸出 (個 十 百 千)
    // 並且用逗號隔開
    
    // 1. 個位數：直接對 10 取餘數 (%)
    // 1234 % 10 = 4
    printf("%d,", num % 10);
    
    // 2. 十位數：先除以 10 變成 123，再對 10 取餘數得到 3
    // (1234 / 10) % 10 = 3
    printf("%d,", (num / 10) % 10);
    
    // 3. 百位數：先除以 100 變成 12，再對 10 取餘數得到 2
    // (1234 / 100) % 10 = 2
    printf("%d,", (num / 100) % 10);
    
    // 4. 千位數：直接除以 1000 得到 1
    // 1234 / 1000 = 1
    // 注意：這是最後一個數字，題目要求最後要有換行 (\n)
    printf("%d\n", num / 1000);

	return 0;
}